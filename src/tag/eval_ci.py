"""PRD-047: Eval CI gate integration.

Runs eval suites as CI gates, posts PR comments, and generates GitHub Actions
workflow YAML for embedding eval checks in CI pipelines.
"""
from __future__ import annotations

import json
import sqlite3
import textwrap
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any


@dataclass
class EvalCIResult:
    passed: bool
    pass_rate: float
    threshold: float
    run_id: str
    suite_path: str
    profile: str
    failures: list[dict] = field(default_factory=list)
    comment_body: str = ""
    judge_pass_rate: float | None = None
    cost_usd: float | None = None


def run_eval_ci(
    conn: sqlite3.Connection,
    suite_path: str,
    profile: str,
    cfg: dict,
    *,
    threshold: float = 0.85,
    post_comment: bool = False,
    repo: str | None = None,
    pr_number: int | None = None,
    judge_model: str | None = None,
    fail_below: float | None = None,
) -> EvalCIResult:
    from tag import eval_framework

    actual_threshold = fail_below if fail_below is not None else threshold

    run_id, summary = eval_framework.create_eval_run(conn, suite_path, profile)
    suite = eval_framework.load_suite(suite_path)
    failures: list[dict] = []
    passed_count = 0

    for case in suite.get("cases", []):
        result = eval_framework.score_case(case, profile, cfg)
        eval_framework.record_case_result(conn, run_id, case, result)
        if result.get("passed"):
            passed_count += 1
        else:
            failures.append({"id": case.get("id","?"), "reason": result.get("reason","")})

    total = len(suite.get("cases", []))
    pass_rate = passed_count / total if total else 0.0
    eval_framework.finalize_eval_run(conn, run_id, pass_rate)

    judge_pass_rate = None
    if judge_model:
        try:
            from tag import eval_judge
            judge_result = eval_judge.run_judge_on_eval(
                conn, run_id, judge_model, None, cfg
            )
            judge_pass_rate = judge_result.pass_rate
        except Exception:
            pass

    passed = pass_rate >= actual_threshold
    comment = format_ci_comment(EvalCIResult(
        passed=passed,
        pass_rate=pass_rate,
        threshold=actual_threshold,
        run_id=run_id,
        suite_path=suite_path,
        profile=profile,
        failures=failures,
        judge_pass_rate=judge_pass_rate,
    ))

    if post_comment and repo and pr_number:
        try:
            from tag.ci import post_pr_comment
            post_pr_comment(repo, pr_number, comment)
        except Exception:
            pass

    return EvalCIResult(
        passed=passed,
        pass_rate=pass_rate,
        threshold=actual_threshold,
        run_id=run_id,
        suite_path=suite_path,
        profile=profile,
        failures=failures,
        comment_body=comment,
        judge_pass_rate=judge_pass_rate,
    )


def format_ci_comment(result: EvalCIResult) -> str:
    status = "✅ PASSED" if result.passed else "❌ FAILED"
    lines = [
        f"## TAG Eval CI Gate {status}",
        "",
        f"| Metric | Value |",
        f"|--------|-------|",
        f"| Suite | `{result.suite_path}` |",
        f"| Profile | `{result.profile}` |",
        f"| Pass Rate | {result.pass_rate:.1%} |",
        f"| Threshold | {result.threshold:.1%} |",
        f"| Run ID | `{result.run_id}` |",
    ]
    if result.judge_pass_rate is not None:
        lines.append(f"| Judge Pass Rate | {result.judge_pass_rate:.1%} |")
    if result.failures:
        lines += ["", "### Failures", ""]
        for f in result.failures[:10]:
            lines.append(f"- `{f['id']}`: {f['reason']}")
        if len(result.failures) > 10:
            lines.append(f"- ... and {len(result.failures)-10} more")
    lines += ["", "*Generated by [TAG](https://github.com/tag-agent/tag)*"]
    return "\n".join(lines)


def scaffold_github_action(
    workflow_type: str = "eval",
    *,
    profile: str = "reviewer",
    threshold: float = 0.85,
) -> str:
    types = {
        "eval": (
            f"tag eval ci --suite tests/eval_suite.yaml --profile {profile} "
            f"--fail-below {threshold}"
        ),
        "review": f"tag ci review --repo ${{{{ github.repository }}}} --pr ${{{{ github.event.number }}}} --post-comment",
        "test-gen": "tag ci test-gen --diff-from-staged --profile coder",
        "fix-vuln": "tag ci fix-vuln --sarif results.sarif --profile security",
    }
    run_cmd = types.get(workflow_type, types["eval"])
    return textwrap.dedent(f"""\
        name: TAG {workflow_type.replace('-', ' ').title()}

        on:
          pull_request:
            branches: [main, master]

        jobs:
          tag-{workflow_type}:
            runs-on: ubuntu-latest
            permissions:
              pull-requests: write
              contents: read

            steps:
              - uses: actions/checkout@v4

              - name: Set up Python
                uses: actions/setup-python@v5
                with:
                  python-version: '3.11'

              - name: Install TAG
                run: pip install tag-agent

              - name: Run TAG {workflow_type.replace('-', ' ').title()}
                env:
                  ANTHROPIC_API_KEY: ${{{{ secrets.ANTHROPIC_API_KEY }}}}
                  GITHUB_TOKEN: ${{{{ secrets.GITHUB_TOKEN }}}}
                run: {run_cmd}
    """)


def install_github_action(
    workflow_type: str = "eval",
    output_dir: Path | None = None,
) -> Path:
    content = scaffold_github_action(workflow_type)
    out_dir = output_dir or Path(".github/workflows")
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"tag-{workflow_type}.yml"
    out_path.write_text(content, encoding="utf-8")
    return out_path
